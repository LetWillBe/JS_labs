//* camelCase
// каждое следующее слово начниается с большой буквы, а первое- с маленькой

//* snake_case
//слова разделяются нижним подчеркинванием (стиль названия папок и файлов)

//* UPPER_SNAKE_CASE
// все слова пишутся капсом и разделяются нижним подчеркиванием
// так определяются переменные которые нельзя переопределять

//* Kebab-case
// слова разделяются дефисом (стиль названия папок и файлов)

//* PascalCase
// все слова начнинаются с большой буквы и пишутся слитно